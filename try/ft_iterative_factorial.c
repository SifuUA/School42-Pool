int		ft_iterative_factorial(int nb)
{
	int i;
	int y;

	y = nb;
	i = 0;
	if (nb == 1)
	{
		return (1);
	}
	else if (nb > 0)
	{
		while (y >= 1)
		{
			i = nb * (nb - 1);
			y--;
		}
		return (i);
	}
	else
		return (0);
}
